
public class App {
    public static void main(String[] args) {
        final int numberToMultiply = 7;
        System.out.println("Tabela de multiplicação do 7 :");

        for (int i = 1; i < 10; i++) {
            System.out.println("7 * " + i + " = " + numberToMultiply * i);
        }
    }
}
